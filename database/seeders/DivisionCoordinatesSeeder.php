    public function run(): void
    {
        // Cache branches keyed by lower-case name
        $branches = DB::table('branches')->get()->keyBy(fn ($b) => strtolower($b->name));

        // Cache divisions per branch: [branch_id => [normalizedKey => division]]
        $divisionCache = [];

        foreach ($this->csvData() as [$csvName, $csvState, $lat, $lng]) {
            $stateName = $this->applyBranchAlias($csvState);
            $divName   = $this->applyDivisionAlias($csvName, $csvState);

            $branch = $branches[strtolower($stateName)] ?? null;
            if (! $branch) {
                continue;
            }

            if (! isset($divisionCache[$branch->id])) {
                $divisionCache[$branch->id] = DB::table('divisions')
                    ->where('branch_id', $branch->id)
                    ->get()
                    ->keyBy(fn ($d) => $this->normalizeKey($d->name));
            }

            $key      = $this->normalizeKey($divName);
            $division = $divisionCache[$branch->id][$key] ?? null;
            if (! $division) {
                continue;
            }

            DB::table('divisions')->where('id', $division->id)->update([
                'latitude'   => $lat,
                'longitude'  => $lng,
                'updated_at' => now(),
            ]);
        }

        // Special fix: Okeigbo (id=3654) gets Ile-Oluji coords + small offset.
        // Ile-Oluji/Okeigbo CSV row: 7.166442, 4.724235 → Ile-Oluji in DB.
        DB::table('divisions')->where('id', 3654)->update([
            'latitude'   => 7.181442,   // 7.166442 + 0.015
            'longitude'  => 4.744235,   // 4.724235 + 0.020
            'updated_at' => now(),
        ]);
    }

